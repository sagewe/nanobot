export const LANG_KEY = "sidekick.lang";
const LEGACY_LANG_KEY = "pikachu.lang";

let _lang =
  localStorage.getItem(LANG_KEY) ||
  localStorage.getItem(LEGACY_LANG_KEY) ||
  (navigator.language && navigator.language.startsWith("zh") ? "zh" : "en");

export function getLang() {
  return _lang;
}

export function setLang(lang) {
  _lang = lang;
  localStorage.setItem(LANG_KEY, lang);
  localStorage.removeItem(LEGACY_LANG_KEY);
}

export function t(key) {
  return (TRANSLATIONS[_lang] || TRANSLATIONS.en)[key] || key;
}

export function tChannel(name) {
  const key = "channel_" + (name || "").toLowerCase();
  return (TRANSLATIONS[_lang] || TRANSLATIONS.en)[key] || name;
}

export function tToolCount(count) {
  if (_lang === "zh") {
    return `\u8c03\u7528\u4e86 ${count} \u4e2a\u5de5\u5177`;
  }
  return `Used ${count} tools`;
}

export function applyI18n() {
  document.documentElement.lang = _lang;
  document.title = t("page_title");
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAriaLabel));
  });
  document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
    el.alt = t(el.dataset.i18nAlt);
  });
  const langToggle = document.getElementById("lang-toggle");
  if (langToggle) langToggle.textContent = t("lang_toggle_label");
}
